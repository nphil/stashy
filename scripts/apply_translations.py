# Load reported untranslated locales from the NEW report file
if not report_file.exists():
    print(f'No {report_file} found; aborting')
    raise SystemExit(1)
report = json.loads(report_file.read_text())

for arb_fname, result in report.items():
    # arb_fname is e.g. "app_de.arb"
    locale = arb_fname.replace('app_', '').replace('.arb', '')
    path = l10n_dir / arb_fname
    if not path.exists():
        print(f'Locale file not found: {path}, skipping')
        continue
    
    data = json.loads(path.read_text())
    updated = False
    
    # Get translations for this exact locale.
    tr = translations.get(locale)
        
    if not tr:
        print(f'No translations provided for {locale}, skipping')
        continue
        
    keys_to_translate = result.get('missing', [])
    for k in keys_to_translate:
        if k in data:
            continue
        if k in tr:
            data[k] = tr[k]
            updated = True
            
    if updated:
        path.write_text(json.dumps(data, ensure_ascii=False, indent=2) + "\n")
        print(f'Updated {path}')
    else:
        print(f'No changes for {path}')

print('Done')
